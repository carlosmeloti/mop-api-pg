package br.embrapa.repository.projections;

public class ResumoModLocal2 {
	
	private Long cdLocal1;
	private Long cdLocal2;
	private String cdEmpresa;
	private String nmlocal2;
	
	public ResumoModLocal2(Long cdLocal1, Long cdLocal2, String cdEmpresa, String nmlocal2) {
		this.cdLocal1 = cdLocal1;
		this.cdLocal2 = cdLocal2;
		this.cdEmpresa = cdEmpresa;
		this.nmlocal2 = nmlocal2;
	}

	public Long getCdLocal1() {
		return cdLocal1;
	}

	public void setCdLocal1(Long cdLocal1) {
		this.cdLocal1 = cdLocal1;
	}

	public Long getCdLocal2() {
		return cdLocal2;
	}

	public void setCdLocal2(Long cdLocal2) {
		this.cdLocal2 = cdLocal2;
	}

	public String getCdEmpresa() {
		return cdEmpresa;
	}

	public void setCdEmpresa(String cdEmpresa) {
		this.cdEmpresa = cdEmpresa;
	}

	public String getNmlocal2() {
		return nmlocal2;
	}

	public void setNmlocal2(String nmlocal2) {
		this.nmlocal2 = nmlocal2;
	}
	
	
	
	
	
	
	
	
}
